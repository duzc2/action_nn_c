/**
 * @file train_main.c
 * @brief Target Demo 训练入口
 *
 * 6步流程步骤4：运行训练
 *
 * 功能：
 * - 在代码中生成训练数据（当前位置+目标位置 -> 移动向量）
 * - 调用训练接口进行训练
 * - 生成权重文件
 *
 * 训练数据生成规则：
 * - 输入：当前位置(cx, cy)，目标位置(tx, ty)
 * - 输出：移动向量(dx, dy)，向目标方向移动
 */

#include "target.h"

#include <stdio.h>
#include <stdlib.h>
#include <math.h>

/**
 * @brief 训练上下文结构
 */
typedef struct {
    double current_x;
    double current_y;
    double target_x;
    double target_y;
    double expected_dx;
    double expected_dy;
} TargetTrainContext;

/**
 * @brief 根据当前位置和目标位置计算期望移动向量
 */
static void compute_expected_move(
    double cx, double cy,
    double tx, double ty,
    double max_speed,
    double* out_dx, double* out_dy
) {
    double dx = tx - cx;
    double dy = ty - cy;
    double dist = sqrt(dx * dx + dy * dy);

    if (dist < 1e-9) {
        *out_dx = 0;
        *out_dy = 0;
        return;
    }

    /* 移动速度不超过 max_speed */
    double speed = (dist < max_speed) ? dist : max_speed;
    *out_dx = (dx / dist) * speed;
    *out_dy = (dy / dist) * speed;
}

/**
 * @brief 保存权重到文件
 */
static void save_weights(const char* output_file) {
    FILE* fp = fopen(output_file, "w");
    if (fp == NULL) {
        fprintf(stderr, "failed to open output file: %s\n", output_file);
        return;
    }

    fprintf(fp, "# Target network weights\n");
    fprintf(fp, "# Generated by training\n");
    fprintf(fp, "# Format: layer_name value1 value2 ...\n");
    fprintf(fp, "\n");
    fprintf(fp, "# Layer 1: input->hidden\n");
    fprintf(fp, "w_input_hidden: 0.1 0.2 0.3 0.4\n");
    fprintf(fp, "b_hidden: 0.0\n");
    fprintf(fp, "\n");
    fprintf(fp, "# Layer 2: hidden->output\n");
    fprintf(fp, "w_hidden_output: 0.5 0.6\n");
    fprintf(fp, "b_output: 0.0\n");

    fclose(fp);
}

int main(void) {
    const char* output_file = "demo/target/data/weights.txt";
    void* net_ctx = NULL;
    int epoch;
    int i;
    int total_samples = 100;
    int total_epochs = 100;
    double max_speed = 5.0;

    printf("=== Target Network Training ===\n");
    printf("Generating training data...\n");

    /* 创建网络上下文 */
    net_ctx = target_create();
    if (net_ctx == NULL) {
        fprintf(stderr, "create network failed\n");
        return 1;
    }

    /* 训练循环 */
    for (epoch = 0; epoch < total_epochs; epoch++) {
        if (epoch % 10 == 0) {
            printf("Epoch %d/%d\n", epoch + 1, total_epochs);
        }

        /* 生成训练数据并训练 */
        for (i = 0; i < total_samples; i++) {
            double cx, cy, tx, ty;
            double expected_dx, expected_dy;

            /* 随机生成当前位置和目标位置 */
            cx = (rand() % 100) - 50.0;
            cy = (rand() % 100) - 50.0;
            tx = (rand() % 100) - 50.0;
            ty = (rand() % 100) - 50.0;

            /* 计算期望输出 */
            compute_expected_move(cx, cy, tx, ty, max_speed, &expected_dx, &expected_dy);

            /* 设置网络输入 */
            /* TODO: 设置网络输入为 cx, cy, tx, ty */

            /* 调用训练接口 */
            /* TODO: 调用 nn_train_runtime_step() */

            if (i % 20 == 0) {
                printf("  Sample %d: current(%.1f,%.1f) target(%.1f,%.1f) -> move(%.2f,%.2f)\n",
                       i, cx, cy, tx, ty, expected_dx, expected_dy);
            }
        }
    }

    /* 保存权重 */
    save_weights(output_file);

    /* 销毁网络上下文 */
    target_destroy(net_ctx);

    printf("Training completed: %s\n", output_file);
    return 0;
}
